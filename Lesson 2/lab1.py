a = int(input("Введите 1ое число: "))
b = int(input("Введите 2ое число: "))
c = int(input("Введите 3ое число: "))
check = int(input("Что считаем?"
                  "\n\t1. Сумма"
                  "\n\t2. Произведение"
                  "\n\tВведите число: "))
print("*" * 30)
if check == 1:
    print(f"Сумма чисел {a}, {b}, {c} равна: {a + b + c}")
elif check == 2:
    print(f"Произведение чисел {a}, {b}, {c} равна: {a * b * c}")
else:
    print("Вы ввели некорректное значение")
