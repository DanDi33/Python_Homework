# Есть три кортежа целых чисел необходимо найти
# элементы, которые есть во всех кортежах.

a = (1, 2, 3, 4, 5, 6, 7, 8, 9)
b = (1, 1, 1, 11, 2, 1, 3, 1)
c = (3, 3, 3, 3, 23, 3, 2, 3)
d = []
print(a, b, c, sep="\n")
for el in a:
    if el in a and el in b and el in c:
        d.append(el)
print("Общими для 3х кортежей являются:", end=" ")
[print(el, end=" ") for el in d]
